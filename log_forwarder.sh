# Configure Logstash-forwarder on client servers

#Logstash-forwarder is a client software which ship logs to a logstash server, it should be installed on all client servers.

sudo wget https://download.elastic.co/logstash-forwarder/binaries/logstash-forwarder_0.4.0_amd64.deb

dpkg -i logstash-forwarder_0.4.0_amd64.deb


